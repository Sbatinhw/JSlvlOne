"use strict";

function Addition(operand1, operand2) {
    return +operand1 + +operand2;
}

function Subtraction(operand1, operand2) {
    return +operand1 - +operand2;
}

function Division(operand1, operand2) {
    return +operand1 / +operand2;
}

function Multiplication(operand1, operand2) {
    return +operand1 * +operand2;
}